package com.example.mathlib

object MathUtils {
    /**
     * Soma dois números inteiros
     * @param a primeiro número
     * @param b segundo número
     * @return soma de a + b
     */
    fun sum(a: Int, b: Int): Int {
        return a + b
    }

    /**
     * Soma dois números decimais
     * @param a primeiro número
     * @param b segundo número
     * @return soma de a + b
     */
    fun sum(a: Double, b: Double): Double {
        return a + b
    }
}